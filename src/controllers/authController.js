const { validationResult } = require('express-validator');
const User = require('../models/User');
const Tenant = require('../models/Tenant');

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res, next) => {
  console.log('Received registration body:', req.body);
  try {
    // Check validation errors
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { firstName, lastName, email, password, role, tenantId, tenantData } = req.body;

    // Check if user exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User already exists with this email'
      });
    }

    let tenant = null;

    // Handle tenant creation for tenant role
    if (role === 'tenant') {
      if (!tenantData) {
        return res.status(400).json({
          success: false,
          message: 'Tenant data is required for tenant registration'
        });
      }

      // Create new tenant
      tenant = await Tenant.create({
        name: tenantData.name,
        subdomain: tenantData.subdomain,
        email: email,
        phone: tenantData.phone,
        business: tenantData.business,
        address: tenantData.address,
        settings: tenantData.settings
      });
    } else if (role === 'customer') {
        tenant = await Tenant.findOne(); 

        if (!tenant) {
          return res.status(400).json({
            success: false,
            message: 'No tenants exist to assign customer to'
          });
        }
      }
     else {
      // For non-tenant roles, tenantId is required
      if (!tenantId) {
        return res.status(400).json({
          success: false,
          message: 'Tenant ID is required for this role'
        });
      }

      tenant = await Tenant.findById(tenantId);
      if (!tenant) {
        return res.status(400).json({
          success: false,
          message: 'Invalid tenant ID'
        });
      }
    }

    // Create user
    const user = await User.create({
      firstName,
      lastName,
      email,
      password,
      role,
      tenant: tenant._id,
      profile: req.body.profile,
      availability: req.body.availability,
      address: req.body.address,
      preferences: req.body.preferences
    });



    sendTokenResponse(user, 201, res);
  } catch (error) {
    if (error.code === 11000) {
      const message = 'Duplicate field value entered';
      return res.status(400).json({
        success: false,
        message
      });
    }
    next(error);
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
exports.login = async (req, res, next) => {
  try {
    // Check validation errors
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { email, password } = req.body;

    // Check for user
    const user = await User.findOne({ email }).select('+password').populate('tenant');

    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Check if password matches
    const isMatch = await user.matchPassword(password);

    if (!isMatch) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Check if user is active
    if (!user.isActive) {
      return res.status(401).json({
        success: false,
        message: 'Account is deactivated'
      });
    }

    // Update last login
    user.lastLogin = new Date();
    await user.save();

    sendTokenResponse(user, 200, res);
  } catch (error) {
    next(error);
  }
};

